# ### Ejercicio 1: Comparativa de Películas (Fácil)

# **Descripción:**
# Crea un programa que se conecte a la API de The Movie Database (TMDb) para comparar dos películas.

# **Requisitos:**
# 1. Regístrate en TMDb y obtén tu clave API.
# 2. Conéctate a la API utilizando tu lenguaje de programación.
# 3. Recupera datos de las películas seleccionadas.

# **Acciones:**
# 1. Accede a la información de ambas películas (título, año, puntuación).
# 2. Compara sus puntuaciones y número de reseñas.
# 3. Muestra los resultados por consola.
# 4. Determina cuál película es más popular según los datos.

# **Conceptos:**
# - Conexión a APIs.
# - Estructuras de datos para almacenar la información.
# - Condicionales para comparación.

# ---

# ### Ejercicio 2: Comparativa de Videojuegos (Intermedio)

# **Descripción:**
# Desarrolla un programa que se conecte a la API de RAWG para comparar dos videojuegos.

# **Requisitos:**
# 1. Regístrate en RAWG y obtén tu clave API.
# 2. Conéctate a la API y selecciona dos videojuegos.
# 3. Recupera datos relevantes como calificaciones, número de reseñas y plataformas.

# **Acciones:**
# 1. Accede a las estadísticas de ambos videojuegos.
# 2. Compara sus calificaciones, número de jugadores y plataformas disponibles.
# 3. Muestra los resultados en la consola.
# 4. Determina cuál videojuego es más popular.

# **Conceptos:**
# - Manejo de respuestas JSON.
# - Estructuras de datos para almacenar y comparar.
# - Uso de funciones para organizar el código.

# ---

# ### Ejercicio 3: Comparativa de Influencers en Redes Sociales (Difícil)

# **Descripción:**
# Desarrolla un programa que se conecte a la API de Instagram o Twitter para comparar dos influencers.

# **Requisitos:**
# 1. Regístrate en la API de la red social elegida y obtén credenciales.
# 2. Conéctate a la API y selecciona dos perfiles de influencers.
# 3. Recupera datos como número de seguidores, interacciones y publicaciones.

# **Acciones:**
# 1. Accede a las estadísticas de ambos influencers.
# 2. Compara el número de seguidores, tasa de interacción y crecimiento mensual.
# 3. Muestra los resultados por consola.
# 4. Desarrolla un criterio para determinar cuál influencer es más popular.

# **Conceptos:**
# - Autenticación con APIs (OAuth).
# - Procesamiento de datos y análisis.
# - Algoritmos para comparación y ranking.

