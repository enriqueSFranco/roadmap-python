# ### Ejercicio 2: Batalla de Magos (Intermedio)

# **Descripción:**
# Crea un programa que simule un duelo entre dos magos. Cada mago puede lanzar hechizos con diferentes efectos.

# **Requisitos:**
# 1. Cada mago tiene puntos de vida, poder mágico y una lista de hechizos.
# 2. El usuario puede elegir el hechizo a lanzar en cada turno.
# 3. Los hechizos pueden causar daño o curar puntos de vida.
# 4. Un mago gana si el otro llega a 0 puntos de vida.

# **Acciones:**
# 1. Crear magos con atributos iniciales.
# 2. Seleccionar hechizos en cada turno.
# 3. Mostrar el resultado de cada hechizo.
# 4. Mostrar la vida de cada mago tras cada turno.
# 5. Mostrar el ganador al final.

# **Conceptos:**
# - Clases para `Mago` y `Hechizo`.
# - Listas para almacenar los hechizos.
# - Condicionales para gestionar efectos de hechizos.

