# ### Ejercicio 1: Carrera de Animales (Fácil)

# **Descripción:**
# Crea un programa que simule una carrera entre diferentes animales. El programa permitirá al usuario seleccionar animales y competir en una carrera.

# **Requisitos:**
# 1. El usuario debe elegir al menos 2 animales (ej.: perro, gato, tortuga).
# 2. Cada animal tiene una velocidad aleatoria entre 1 y 10.
# 3. La carrera avanza en turnos, donde cada animal avanza según su velocidad.
# 4. El programa muestra el avance de cada animal en cada turno.
# 5. El primero en cruzar la meta gana.

# **Acciones:**
# 1. Seleccionar animales.
# 2. Iniciar carrera.
# 3. Mostrar el avance en cada turno.
# 4. Mostrar el ganador.

# **Conceptos:**
# - Clases para `Animal`.
# - Generación de números aleatorios.
# - Bucles para simular los turnos.

