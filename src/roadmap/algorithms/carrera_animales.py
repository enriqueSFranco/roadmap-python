# ### Ejercicio 3: Simulación de Ecosistema (Difícil)

# **Descripción:**
# Desarrolla un sistema que simule un ecosistema con diferentes especies de animales y plantas. Cada especie interactúa y afecta a las demás.

# **Requisitos:**
# 1. Cada especie tiene atributos como salud, reproducción y alimentación.
# 2. Se simulan ciclos de vida donde cada especie puede reproducirse, alimentarse o morir.
# 3. El usuario puede ajustar factores ambientales (ej.: clima) que afectan a las especies.
# 4. Mostrar el estado del ecosistema tras cada ciclo.

# **Acciones:**
# 1. Crear especies y establecer interacciones.
# 2. Iniciar ciclos de vida.
# 3. Mostrar la evolución del ecosistema.
# 4. Ajustar factores ambientales.
# 5. Mostrar un resumen final del estado del ecosistema.

# **Conceptos:**
# - Clases para `Especie` y `Ecosistema`.
# - Métodos para gestionar interacciones.
# - Generación de eventos aleatorios para simular variabilidad en el ecosistema.
