# ### Ejercicio 3: Sistema de Reserva de Vuelos (Difícil)

# **Descripción:**
# Desarrolla un sistema que gestione la reserva de vuelos en una aerolínea. El sistema debe permitir registrar vuelos, pasajeros y realizar reservas.

# **Requisitos:**
# 1. Registrar vuelos con número de vuelo, destino, origen y capacidad.
# 2. Registrar pasajeros con nombre, ID y contacto.
# 3. Permitir realizar reservas y verificar disponibilidad.
# 4. Mostrar información de los vuelos y los pasajeros reservados.

# **Acciones:**
# 1. Agregar vuelo.
# 2. Agregar pasajero.
# 3. Realizar reserva.
# 4. Mostrar información de vuelos y reservas.
# 5. Salir del programa.

# **Conceptos:**
# - Clases para `Vuelo`, `Pasajero`, y `Reserva`.
# - Listas y diccionarios para gestionar la información.
# - Excepciones para manejar errores (por ejemplo, cuando no hay disponibilidad).

