# ### Ejercicio 1: Registro de Libros (Fácil)

# **Descripción:**
# Crea un programa que permita gestionar una biblioteca. El programa debe permitir al usuario registrar libros y realizar consultas sobre ellos.

# **Requisitos:**
# 1. Registrar libros con título, autor y año de publicación.
# 2. Mostrar todos los libros registrados.
# 3. Permitir buscar libros por título o autor.

# **Acciones:**
# 1. Agregar libro.
# 2. Listar libros.
# 3. Buscar libro.
# 4. Salir del programa.

# **Conceptos:**
# - Clases y objetos.
# - Listas para almacenar libros.
