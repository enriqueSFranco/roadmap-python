# ### Ejercicio 2: Gestión de Estudiantes (Intermedio)

# **Descripción:**
# Crea un sistema para gestionar estudiantes en una escuela. El sistema debe permitir registrar estudiantes, asignarles materias y calcular sus promedios.

# **Requisitos:**
# 1. Registrar estudiantes con nombre, ID y lista de calificaciones.
# 2. Asignar materias a los estudiantes.
# 3. Calcular el promedio de calificaciones de cada estudiante.
# 4. Mostrar la lista de estudiantes junto con sus promedios.

# **Acciones:**
# 1. Agregar estudiante.
# 2. Asignar materia y calificaciones.
# 3. Listar estudiantes y promedios.
# 4. Salir del programa.

# **Conceptos:**
# - Clases y herencia (puedes crear una clase base `Persona`).
# - Diccionarios para almacenar materias y calificaciones.
